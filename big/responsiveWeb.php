    <p>Should I develop my project as a web application, or as a native application? It seems as though you would have a more definitive answer if you asked, “What’s better, pizza or tacos?” Stop thinking about it, you’ll only get hungry. </p>

<p>These questions seem to have no clear answer, or at least, the clear answer must take into account the limitations of the scenario or the people involved, the desired effect. </p>

<p>Developing an app to be a responsive web page means writing the content, layout and style to automatically adjust to the type of device being used to access it. Developing a project for native means that users will have to download the application to use it. Each of these approaches have their advantages and disadvantages as one would expect. </p>

<p>Making a responsive app means that any device of any operating system can access it. It uses well established and widely used languages like HTML, CSS and JavaScript to create an adaptive website that is accessed through the browser. This makes it widely available, but it does limit its capabilities. </p>

<p>A native application can take full advantage of the resources of the devices being used, including processing power, memory, storage, as well as the camera and certain touch features. Native applications in general are faster and more capable. One downside is that you then must write and maintain two codebases, one for Android, and one for iOS. Another downside is that you must actually find a way to get users to download your app in the first place. </p>

<p>Native apps only reach about 3 millions uniges users per month, compared to the almost 9 million unique users that access mobile websites [1]. Conversely, when users do end up using a mobile application, they tend to spend far more time using it than they do using mobile websites [1]. </p>

<p>According to Nick Babich of Adobe [2], as well as the majority of other resources available, it is a good idea to have both a native application and a web application, and if that is not possible, then it ultimately depends upon you or your client’s needs. Looks like we’re having pizza and tacos tonight. </p>

