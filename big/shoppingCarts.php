<p>In the world of web design and development, odds are great that at some point, we will be making an e-commerce website. The obvious aspect these sites all share in common is a shopping cart, or some sort of checkout system. </p>

<p>We can all use a shopping cart fairly well I am certain, but designing and implementing one is a complex process that can leave our clients and their customers frustrated if things don’t work smoothly every time. </p>

<p>A shopping cart on an e-commerce site is more than just a place to hold products until the time you purchase them, it handles everything involved in making a purchase. To build off the metaphor in the blogpost from Dinarys, “It also represents the shelves, the sale and clearance signs, cash registers and credit card machines which gather data and transfer it to banks” [1].</p>

<p>It is very possible to build a shopping cart or checkout interface from scratch. <a href="https://codeshack.io/shopping-cart-system-php-mysql/">This tutorial</a> from Codeshack provides a step by process with code examples to do so, using HTML, CSS, a server-side scripting language like PHP and a database with MySQL for querying the database. Even with this extensive guide, though, it is still required that you integrate some type of authorized payment system such as PayPal. Codeshack also provides a guide for <a href="https://codeshack.io/how-to-integrate-paypal-php-mysql/">integrating with PayPal’s API.</a>
Unless your client has the bankroll to hire experienced developers or even a team of developers, it may be in your best interest as their designer to use an established e-commerce plugin or extension. There are hundreds of them out there, even free and open-source ones like <a href=”https://www.opencart.com/index.php?route=common/home”>Open Cart</a>. Especially if your client is just starting out, this time saving and money saving approach may be best until they have a better idea of the dynamics of their business, if they need a more custom-fit shopping experience for their own users, or if a generic plugin suits their needs just fine.</p> 
